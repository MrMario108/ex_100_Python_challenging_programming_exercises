# Write a program which accepts a string as input to print "Yes" if the string
# is "yes" or "YES" or "Yes", otherwise print "No".

# Hints:

# Use if statement to judge condition.

value = input("Enter: ")
if value == "yes" or value == "YES" or value == "Yes":
    print("Yes")
else:
    print("No")

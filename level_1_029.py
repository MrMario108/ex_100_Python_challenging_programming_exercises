# Define a function that can receive two integral numbers in string form and
# compute their sum and then print it in console.

# Hints:

# Use int() to convert a string to integer.


def int_to_string(numbers_list):
    return int(numbers_list[0]) + int(numbers_list[1])


numbers_list = [6666661, 55]

print(int_to_string(numbers_list))
